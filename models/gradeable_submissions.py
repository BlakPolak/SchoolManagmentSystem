class GradeableSubmissions:
    def __init__(self, assignment_id, assignment_name, assignment_delivery_date, student_name,
                 student_surname, submission_date, submission_id):
        self.assignment_id = assignment_id
        self.assignment_name = assignment_name
        self.assignment_delivery_date = assignment_delivery_date
        self.student_name = student_name
        self.student_surname = student_surname
        self.submission_date = submission_date
        self.submission_id = submission_id